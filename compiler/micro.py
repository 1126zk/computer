SU = 0x40  # ALU_SUBSTRACT
LO = 0x80
FG = 0x100  # ALU_OUT

AI = 0x200  # A REGISTER IN
AO = 0x400  # A REGISTER OUT
BI = 0x800  # B REGISTER IN
BO = 0x1000  # B REGISTER OUT
CI = 0x2000  # ACCUMULATE REGISTER IN
CO = 0x4000  # ACCUMULATE REGISTER OUT

MI = 0x8000  # MEMORY ADDRESS REGISTER IN
MO = 0x10000  # MEMORY ADDRESS REGISTER OUT
RI = 0x20000  # MEMORY IN
RO = 0x40000  # MEMORY OUT
DI = 0x80000  # DATA ADDRESS REGISTER OUT
DO = 0x100000  # DATA ADDRESS REGISTER OUT
GI = 0x200000  # GENERAL REGISTER IN
GO = 0x400000  # GERNERAL REGISTER OUT

OI = 0x800000  # OUTPUT IN
OO = 0x1000000  # OUTPUT OUT


